# Make a list of magician's names.
# Pass the list to a function called show_magicians(), which prints the name
# of each magician in the list.

def show_magicians(listOfMagicians):
    for x in listOfMagicians:
        print(x)

group = ['Mr. Bennett', 'Houdini', 'Penn Jillette']
show_magicians(group)
